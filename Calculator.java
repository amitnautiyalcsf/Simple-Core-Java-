public class Main{

     public static int add(int a, int b) {
         return a+b;
     }
     public static int sub(int c, int d) {
         
         return c-d; 
     }
     public static int mult(int f, int g) {
         
         return f*g;
     }
     public static int div(int h, int i) {
         
         return h/i;
     }
     
     public static void main(String []args){
        System.out.println(add(3,2));
        System.out.println(sub(3,2));
        System.out.println(mult(3,2));
        System.out.println(div(4,2));
     }
}
